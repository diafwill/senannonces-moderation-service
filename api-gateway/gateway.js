// api-gateway/gateway.js
require('dotenv').config({ path: '../moderation-service/.env' });
const express = require('express');
const { createProxyMiddleware } = require('http-proxy-middleware');
const cors = require('cors');
const morgan = require('morgan');
const rateLimit = require('express-rate-limit');

const app = express();
const GATEWAY_PORT = process.env.GATEWAY_PORT || 3000;

// в”Ђв”Ђв”Ђ URLs des microservices в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const SERVICES = {
  annonce:    process.env.ANNONCE_SERVICE_URL    || 'http://localhost:8080',  // Spring Boot
  moderation: `http://localhost:${process.env.PORT || 3001}`,                // Node.js
};

// в”Ђв”Ђв”Ђ Rate Limiting в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100,
  standardHeaders: true,
  legacyHeaders: false,
  message: { success: false, message: 'Trop de requГЄtes. RГ©essayez dans 15 minutes.' },
});

// в”Ђв”Ђв”Ђ Middlewares globaux в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.use(cors());
app.use(morgan('dev'));
app.use(limiter);

// в”Ђв”Ђв”Ђ Logging des requГЄtes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.use((req, res, next) => {
  console.log(`[GATEWAY] ${req.method} ${req.path} в†’ Routage en cours...`);
  next();
});

// в”Ђв”Ђв”Ђ Route racine du Gateway в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.get('/', (req, res) => {
  res.json({
    service: 'SenAnnonces - API Gateway',
    version: '1.0.0',
    routes: {
      '/annonces-service/*': `в†’ Annonce Service (Spring Boot) : ${SERVICES.annonce}`,
      '/moderation/*':       `в†’ Moderation Service (Node.js)  : ${SERVICES.moderation}`,
      '/health':             `в†’ Г‰tat du Gateway`,
    },
    documentation: {
      moderationSwagger: `${SERVICES.moderation}/api-docs`,
    },
  });
});

// в”Ђв”Ђв”Ђ Health du Gateway в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.get('/health', (req, res) => {
  res.json({
    gateway: 'UP',
    timestamp: new Date().toISOString(),
    services: {
      annonceService:    { url: SERVICES.annonce,     status: 'configurГ©' },
      moderationService: { url: SERVICES.moderation,  status: 'configurГ©' },
    },
  });
});

// в”Ђв”Ђв”Ђ Proxy в†’ Annonce Service (Spring Boot) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.use(
  '/annonces-service',
  createProxyMiddleware({
    target: SERVICES.annonce,
    changeOrigin: true,
    pathRewrite: { '^/annonces-service': '' },
    on: {
      error: (err, req, res) => {
        console.error('[GATEWAY] Annonce Service indisponible :', err.message);
        res.status(503).json({
          success: false,
          message: 'Annonce Service (Spring Boot) indisponible.',
          hint: `VГ©rifiez que le service tourne sur ${SERVICES.annonce}`,
        });
      },
    },
  })
);

// в”Ђв”Ђв”Ђ Proxy в†’ Moderation Service (Node.js) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.use(
  '/moderation',
  createProxyMiddleware({
    target: SERVICES.moderation,
    changeOrigin: true,
    pathRewrite: { '^/moderation': '' },
    on: {
      error: (err, req, res) => {
        console.error('[GATEWAY] Moderation Service indisponible :', err.message);
        res.status(503).json({
          success: false,
          message: 'Moderation Service (Node.js) indisponible.',
          hint: `VГ©rifiez que le service tourne sur ${SERVICES.moderation}`,
        });
      },
    },
  })
);

// в”Ђв”Ђв”Ђ 404 Gateway в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
app.use((req, res) => {
  res.status(404).json({
    success: false,
    message: `Route ${req.method} ${req.path} inconnue du Gateway.`,
    routesDisponibles: ['/annonces-service/*', '/moderation/*', '/health'],
  });
});

// в”Ђв”Ђв”Ђ DГ©marrage в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
